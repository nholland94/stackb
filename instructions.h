#ifndef __INSTRUCTIONS_H__
#define __INSTRUCTIONS_H__

typedef uint8_t instr;

#define INSTR_DROP1       0x00
#define INSTR_DROP2       0x01
#define INSTR_DROP3       0x02
#define INSTR_DROPN       0x03
#define INSTR_DUP1        0x04
#define INSTR_DUP2        0x05
#define INSTR_DUP3        0x06
#define INSTR_DUPN        0x07
#define INSTR_OVER        0x08
#define INSTR_PICK        0x09
#define INSTR_ROTL        0x0a
#define INSTR_ROTR        0x0b
#define INSTR_SWAP        0x0c

#define INSTR_ADD         0x20
#define INSTR_SUB         0x21
#define INSTR_MUL         0x22
#define INSTR_DIV         0x23

#define INSTR_PUSH1_INT   0x60
#define INSTR_PUSH2_INT   0x61
#define INSTR_PUSH3_INT   0x62
#define INSTR_PUSHN_INT   0x63
#define INSTR_PUSH1_FLOAT 0x64
#define INSTR_PUSH2_FLOAT 0x65
#define INSTR_PUSH3_FLOAT 0x66
#define INSTR_PUSHN_FLOAT 0x67
#define INSTR_PUSH1_QUOTE 0x68
#define INSTR_PUSH2_QUOTE 0x69
#define INSTR_PUSH3_QUOTE 0x6a
#define INSTR_PUSHN_QUOTE 0x6b
#define INSTR_PUSH1_QREF  0x6c
#define INSTR_PUSH2_QREF  0x6d
#define INSTR_PUSH3_QREF  0x6e
#define INSTR_PUSHN_QREF  0x6f

#define INSTR_CALL        0x80
#define INSTR_KEEP1       0x81
#define INSTR_KEEP2       0x82
#define INSTR_KEEP3       0x83

#define INSTR_IF          0xa0

#endif
